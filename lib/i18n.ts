export function useTranslation(lang: string) {
  return {
    t: (key: string) => {
      const keys = key.split(".")
      let value: any = translations[lang as keyof typeof translations]

      for (const k of keys) {
        value = value?.[k]
      }

      return value || key
    },
  }
}
